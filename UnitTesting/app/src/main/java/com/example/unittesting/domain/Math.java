package com.example.unittesting.domain;

public class Math {

    public int addFunction(int a, int b){
        return a + b;
    }

    public int subtractFunction(int a, int b){
        return  a - b;
    }


    public int multiplication(int a , int b){
        return a * b;
    }


    public int divideFunction(int a, int b){
        return a / b;
    }
}
